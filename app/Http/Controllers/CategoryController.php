     * @return \Illuminate\Http\Response
     */
    public function index()

    {   

        $s = request()->input('s');
        if($s != ''){
            $channels = \DB::table('categories')
                        ->where('name','LIKE','%'.$s.'%')
                        ->paginate(10);
        }else{

        $channels = Category::latest()->paginate(10);
        }
        return view('admin.category.index',compact('channels'));
    }


